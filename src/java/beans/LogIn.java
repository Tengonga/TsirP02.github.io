package beans;

import javax.inject.Named;
import javax.enterprise.context.Dependent;

/**
 *
 * @author jtoralo
 */
@Named(value = "logIn")
@Dependent
public class LogIn {
    private String name, pwd;

    public LogIn() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }
    
}
